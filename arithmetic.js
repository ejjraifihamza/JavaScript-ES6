/*
 * Addition
 */
export const add = (x, y) => parseFloat(x) + parseFloat(y);

/*
 * Multiplication
 */
const mul = (x, y) => parseFloat(x) * parseFloat(y);

/*
 * Division
 */
const divide = (x, y) => parseFloat(x) / parseFloat(y);

/*
 * Substraction
 */
export const sub = (x, y) => parseFloat(x) - parseFloat(y);
